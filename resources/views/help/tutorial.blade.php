<h6> Jak zdobyć licencję trenerską? </h6>

<p>Aby umożliwić Ci udział w naszych rozgrywkach, musimy mieć pewność, że rozumiesz zasady panujące w Menago League.</p>
<p>tym celu każdy gracz rozpoczyna swoją przygodę od rozegrania meczu towarzyskiego z innym użytkownikiem.</p>
<p>Jeśli masz już sparing partnera możesz zaprosić go do meczu używając formularza zaznaczonego w poniższej ilustracji
    na czerwono, wybierając przeciwnika z listy i klikając "zaproś".</p>
<p> Jeśli jeszcze nie masz sparing partnera, z pewnością znajdziesz go na naszym kanale Discord lub grupie na Facebooku
    (linki zaznaczone na zielono i niebiesko)</p>

